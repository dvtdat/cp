void solve() {
    string s; cin >> s;
    ll n = s.size();
    ll pos; cin >> pos;
    pos--;

    stack<char> st;

    s += "$";
    for (char c : s) {
        while (pos >= n && st.size() > 0 && st.top() > c) {
            pos -= n--;
            st.pop();
        }

        st.push(c);
    }

    pos = st.size() - pos - 1;
    while (pos > 0) st.pop(), pos--;
    cout << st.top();
}

