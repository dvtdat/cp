int add(int x, int y)
{
    return ((x + y) % MOD + MOD) % MOD;
}

int mul(int x, int y)
{
    return x * 1ll * y % MOD;   
}

int binpow(int x, int y)
{
    int z = 1;
    while(y)
    {
        if(y % 2 == 1) z = mul(z, x);
        x = mul(x, x);
        y /= 2;
    }
    return z;
}

int inv(int x)
{
    return binpow(x, MOD - 2);    
}

int divide(int x, int y)
{
    return mul(x, inv(y));
}


int fact(int n)
{
   if (n == 0 || n == 1) return 1;
   else return n = mul(n, fact(n - 1));
}

void solve()
{
    int n; cin >> n;
    ll res = 1;

    for (int i = 0; i < n / 3; ++i)
    {
        vector<int> a(3);
        cin >> a[0] >> a[1] >> a[2];
        sort(a.begin(), a.end());
        if (a[0] == a[1] && a[1] == a[2]) res = mul(res, 3);
        else if (a[0] == a[1]) res = mul(res, 2);
    }

    res = mul(res, fact(n / 3));
    res = divide(res, mul(fact(n / 6), fact(n / 3 - n / 6)));

    cout << res << '\n';
}

