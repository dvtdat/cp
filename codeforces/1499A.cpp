void solve()
{
    int n, k1, k2; cin >> n >> k1 >> k2;
    int a, b; cin >> a >> b;

    int wh = min(k1, k2);
    int bl = n - max(k1, k2);
    int rest = abs(k1 - k2);
    rest = rest / 2;

    if (rest >= a - wh && rest >= b - bl) cout << "yes\n";
    else cout << "no\n";
}

