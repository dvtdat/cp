void solve()
{
    int n; cin >> n;
    string s; cin >> s;

    int tmp = 1, mx = 1;
    for (int i = 1; i < n; ++i)
    {
        if (s[i] != s[i - 1]) tmp = 1;
        else tmp++;
        mx = max(mx, tmp);
    }

    cout << mx + 1 << '\n';
}

